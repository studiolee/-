
public class Student {
	private long studentID;
	private String name;
	public void setStudentID(long studentID){
		this.studentID = studentID;
	}
	public long getStudentID(){
		return studentID;
	}
	public void setName(String name){
		this.name = name;
	}
	public String getName(){
		return name;
	}

}
